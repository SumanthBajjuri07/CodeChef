

#include <iostream>
using namespace std;

int main() {
	// your code goes here
	int t;
	cin>>t;
	while(t--)
	{
	    string J,S;
	    cin>>J;
	    cin>>S;
	    int count=0;
	    for(int i=0; i<S.size(); i++)
	    {
	        for(int j=0; j<J.size(); j++)
	        {
	            if(S[i]==J[j] )
	            {
	                count++;
                    break;
	            }
	        }
	    }
	    cout<<count<<endl;
	}
	return 0;
}
